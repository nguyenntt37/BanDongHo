        kh.setHo(txtHoKH.getText());
        kh.setQuocGia(txtQGKH.getText());
        kh.setSdt(txtSDTKH.getText());
        kh.setTen(txtTenKH.getText());
        kh.setTenDem(txtTenDemKH.getText());
        kh.setThanhPho(txtTPKH.getText());
