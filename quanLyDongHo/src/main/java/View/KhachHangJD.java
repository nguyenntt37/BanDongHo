    public void showTB(List<KhachHangCutoms> kh) {
        listKH = new KhachHangRespository().getAllKH();
        dtm.setRowCount(0);
        for (KhachHangCutoms khr : listKH) {
            dtm.addRow(khr.toDataRow());
        }
    }

