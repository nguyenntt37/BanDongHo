    private void changePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordActionPerformed
        String ma = Login.getCurrentLoginUsername();
        String mkCu = txtOldPass.getText();
        String mkMoi = txtNewPass.getText();
        String mkMoi2 =txtNewPass2.getText();
        NhanVien nv = new NhanVien();
        nv = service.getByMaNV(ma);
        if(!nv.getMatKhau().equals(mkCu)){
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu cũ không khớp");
            return;
        }
        else if(!mkMoi.equals(mkMoi2)){
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu nhập lại không chính xác");
            return;
        }
        else if(mkCu.equals(mkMoi)){
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu cũ không nên trùng với mật khẩu mới!");
            return;
        }
        JOptionPane.showMessageDialog(rootPane, service.updateMatKhau(nv, ma, mkMoi));
        this.dispose();
    }//GEN-LAST:event_changePasswordActionPerformed

