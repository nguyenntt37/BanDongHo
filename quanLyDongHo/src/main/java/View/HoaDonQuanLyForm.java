        hdct.setChiTietSP(ctsp);
        //Them trangThai
        hdct.setTrangthai(trangThai.getSelectedIndex());
        JOptionPane.showMessageDialog(rootPane, service.updateHoaDonCT(hdct, hd, hdc2.getId(), idCTSP, ngayTao, ngayTT, ngayShip, ngayNhan, tenNguoiNhan, soLuong, tinhTrang ,donGia, sdt, diaChi));
