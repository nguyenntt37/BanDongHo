    private void loadTinhTrang(){
        if(trangThai.getSelectedIndex()==0){
            loadTable(listHoaDonHuy);
        }
        else if(trangThai.getSelectedIndex()==1){
            loadTable(listHoaDonThanhToan);
        }
        else if(trangThai.getSelectedIndex()==2){
            loadTable(listHoaDonGiao);
        }
    }
            
    private String setTinhTrang(List<HoaDonCustom2>list){
        String tinhTrang ="";
        for(HoaDonCustom2 hd : list){
            if(hd.getTrangThai()==0){
                tinhTrang = "Đã hủy";
            }
            else if(hd.getTrangThai()==1){
                tinhTrang = "Đã thanh toán";
            }
            else if(hd.getTrangThai()==2){
                tinhTrang =  "Đang giao";
            }
        }
        return tinhTrang;
    }
    
    private void duyetTinhTrang(List<HoaDonCustom2>list){
        for(HoaDonCustom2 hd : list){
            listHoaDonHuy.remove(hd);
            listHoaDonGiao.remove(hd);
            listHoaDonThanhToan.remove(hd);
        }
        for(HoaDonCustom2 hd : list){
            if(null!=hd.getTrangThai())switch (hd.getTrangThai()) {
                case 0:
                    listHoaDonHuy.add(hd);
                    break;
                case 1:
                    listHoaDonThanhToan.add(hd);
                    
                    break;
                case 2:
                    listHoaDonGiao.add(hd);
                    
                    break;
                default:
                    break;
            }
            
        }
    }
    

            
    private void loadTable(List<HoaDonCustom2>list){
        dtm.setRowCount(0);
        for(HoaDonCustom2 hd : list){
            dtm.addRow(new Object[]{
                hd.getMa(),hd.getTenSP(),hd.getNgayTao(),hd.getNgayThanhToan(),hd.getNgayShip(),hd.getNgayNhan(),hd.getTenNguoiNhan(),hd.getDiaChi(),hd.getSdt(),hd.getSoLuong(),hd.getDongia(),setTinhTrang(list)
            });
        } 
    }
    
    private int getIdCTSP(String tenSP){
        int idSP=10000;
        for(ChiTietSPCustom ctsp : listChiTietSP){
            if(ctsp.getTenSP().equals(tenSP)){
                idSP=ctsp.getId();
            }
        }
        return idSP;
    }

