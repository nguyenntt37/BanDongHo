    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int index = table.getSelectedRow();
        HoaDonCustom2 hdc2 = new HoaDonCustom2();
        if(trangThai.getSelectedIndex()==0){
            hdc2 = listHoaDonHuy.get(index);
            radioHuy.setSelected(true);
        }
        else if(trangThai.getSelectedIndex()==1){
            hdc2 = listHoaDonThanhToan.get(index);
            radioDaThanhToan.setSelected(true);
        }
        else if(trangThai.getSelectedIndex()==2 ){
            hdc2 = listHoaDonGiao.get(index);
            radioDangGiao.setSelected(true);
        }
        txtDiaChi.setText(hdc2.getDiaChi());
        txtDonGia.setText(String.valueOf(hdc2.getDongia()));
        txtMaHD.setText(hdc2.getMa());
        txtNgayShip.setText(hdc2.getNgayShip());
        txtNgayTao.setText(hdc2.getNgayTao());
        txtNgayThanhToan.setText(hdc2.getNgayThanhToan());
        txtNgaynhan.setText(hdc2.getNgayNhan());
        txtSoLuong.setText(String.valueOf(hdc2.getSoLuong()));
        txtSodienThoai.setText(hdc2.getSdt());
        txtTenNguoiNhan.setText(hdc2.getTenNguoiNhan());
        tenSP.setSelectedItem(hdc2.getTenSP());
    }//GEN-LAST:event_tableMouseClicked

