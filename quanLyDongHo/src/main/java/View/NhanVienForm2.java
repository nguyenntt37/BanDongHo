    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int index = table.getSelectedRow();
        List<NhanVienCustom> listNVDangLam = new ArrayList<>();
        for (NhanVienCustom nv : listNhanVien) {
            if (nv.getTrangThai() == 1) {
                listNVDangLam.add(nv);
            }
        }
        NhanVienCustom nv = listNVDangLam.get(index);
        txtMaNV.setText(nv.getMaNV());
        String[] part = nv.getHoTen().split(" ");
        txtHo.setText(part[0]);
        txtTenDem.setText(part[1]);
        txtTenNV.setText(part[2]);
        txtNgaySinh.setText(nv.getNgaySinh());
        txtSDT.setText(nv.getSoDT());
        if (nv.getGioiTinh() == "Nam") {
            radioNam.setSelected(true);
        } else {
            radioNu.setSelected(true);
        }
        txtDiaChi.setText(nv.getDiaChi());
        if (nv.getTrangThai() == 0) {
            radioDaNghi.setSelected(true);
        } else
            radioDangLam.setSelected(true);
    }//GEN-LAST:event_tableMouseClicked

