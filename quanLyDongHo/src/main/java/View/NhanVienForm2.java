    private void loadTable(List<NhanVienCustom> list) {
        dtm.setRowCount(0);
        dtm2.setRowCount(0);
        for (NhanVienCustom nv : list) {
            if (nv.getTrangThai() == 1) {
                dtm.addRow(new Object[]{
                    nv.getMaNV(),
                    nv.getHoTen(),
                    nv.getGioiTinh(),
                    nv.getNgaySinh(),
                    nv.getDiaChi(),
                    nv.getSoDT(),
                    nv.getTrangThai() == 1 ? "Đang làm" : "Đã nghỉ việc"
                });
            } else {
                dtm2.addRow(new Object[]{
                    nv.getMaNV(),
                    nv.getHoTen(),
                    nv.getGioiTinh(),
                    nv.getNgaySinh(),
                    nv.getDiaChi(),
                    nv.getSoDT(),
                    nv.getTrangThai() == 1 ? "Đang làm" : "Đã nghỉ việc"
                });
            }
        }

    }

